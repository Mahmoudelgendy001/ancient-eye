@app.route("/predict", methods=["POST"])
def predict():
    if "file" not in request.files:
        return jsonify({"error": "No file"}), 400

    file = request.files["file"]
    img  = Image.open(file.stream).convert("RGB")

    # preview base64
    buf = BytesIO()
    img.save(buf, format="JPEG", quality=85)
    img_b64 = base64.b64encode(buf.getvalue()).decode()

    # preprocess
    img_resized = img.resize(IMG_SIZE)
    arr = np.array(img_resized).astype(np.float32)
    arr = np.expand_dims(arr, 0)
    arr = preprocess_input(arr)

    preds      = model.predict(arr, verbose=0)[0]
    class_id   = int(np.argmax(preds))
    confidence = float(np.max(preds))
    label      = id2label[class_id]
    info       = info_dict.get(label, {"quick_summary": "", "emoji": "❓"})

    top3 = [
        {"label": id2label[int(i)].replace("_", " "),
         "prob":  round(float(preds[i]) * 100, 1)}
        for i in np.argsort(preds)[::-1][:3]
    ]

    return jsonify({
        "label":            label.replace("_", " "),
        "label_ar":         info.get("name_ar", ""),
        "confidence":       round(confidence * 100, 1),
        "below_threshold":  confidence < CONFIDENCE_THRESHOLD,
        "emoji":            info.get("emoji", "🏛️"),
        "period":           info.get("period", ""),
        "category":         info.get("category", ""),
        "location":         info.get("location", ""),
        "quick_summary":    info.get("quick_summary", ""),
        "why_it_matters":   info.get("why_it_matters", ""),
        "story":            info.get("story", ""),
        "death":            info.get("death", ""),
        "fun_fact":         info.get("fun_fact", ""),
        "ai_hint":          info.get("ai_hint", ""),
        "top3":             top3,
        "image_b64":        img_b64,
    })

